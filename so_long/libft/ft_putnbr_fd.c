#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	ch;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
		ft_putnbr_fd(n, fd);
	}
	else
	{
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		ch = (n % 10) + '0';
		write(fd, &ch, 1);
	}
}
